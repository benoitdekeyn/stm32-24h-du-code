void HAL_GPIO_EXTI_Rising_Callback(uint16_t GPIO_Pin)
{
	HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_12);
// 1. On vérifie que c'est bien notre bouton sur PD1 (PIN_1) qui a déclenché l'alarme
  if(GPIO_Pin == GPIO_PIN_1)
  {
    // 2. On regarde l'heure actuelle en millisecondes
    uint32_t interrupt_time = HAL_GetTick();

    // 3. Le Filtre Anti-Rebond : Si moins de 200ms se sont écoulées, on ignore !
    if ((interrupt_time - last_interrupt_time) > 200)
    {
      // On passe à la couleur suivante
      selector = (ColorState)((selector + 1) % 4);

      // On applique les tensions aux broches selon l'état
      if (selector == OFF) {
        HAL_GPIO_WritePin(GPIOH, GPIO_PIN_6, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_11, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, GPIO_PIN_RESET);
      }
      else if (selector == RED) {
        HAL_GPIO_WritePin(GPIOH, GPIO_PIN_6, GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_11, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, GPIO_PIN_RESET);
      }
      else if (selector == GREEN) {
        HAL_GPIO_WritePin(GPIOH, GPIO_PIN_6, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_11, GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, GPIO_PIN_RESET);
      }
      else if (selector == BLUE) {
        HAL_GPIO_WritePin(GPIOH, GPIO_PIN_6, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_11, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, GPIO_PIN_SET);
      }

      // On met à jour l'heure de référence pour le prochain appui
      last_interrupt_time = interrupt_time;
    }
  }
}

